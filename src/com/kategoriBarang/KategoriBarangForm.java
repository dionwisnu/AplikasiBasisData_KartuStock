    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        try {
            String kodeKategori = textKodeKategori.getText();
            String namaKategori = textNamaKategori.getText();
            KategoriBarang kategori = new KategoriBarang();
            kategori.setKodeKategori(kodeKategori);
            kategori.setNamaKategori(namaKategori);
            KategoriKontrol.getKoneksiKategori().tambahKategoriPrepared(kategori);
            JOptionPane.showMessageDialog(rootPane, "Data Berhasil Disimpan");
            textKodeKategori.requestFocus();
            LihatKategori();
            textKodeKategori.setText("");
            textNamaKategori.setText("");
            tombolSimpan.setEnabled(false);
            tombolHapus.setEnabled(false);

        } catch (SQLException ex) {
            Logger.getLogger(KategoriBarangForm.class
                    .getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,
                    "Data Gagal Disimpan");
        }
    }//GEN-LAST:event_tombolSimpanActionPerformed

    private void tableKategoriBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKategoriBarangMouseClicked
        int baris = tableKategoriBarang.getSelectedRow();
        String kode = tableKategoriBarang.getValueAt(baris, 0).toString();
        String nama = tableKategoriBarang.getValueAt(baris, 1).toString();

        textKodeKategori.setText(kode);
        textNamaKategori.setText(nama);
        tombolUpdate.setEnabled(true);
        tombolSimpan.setEnabled(false);
        tombolHapus.setEnabled(true);

    }//GEN-LAST:event_tableKategoriBarangMouseClicked

