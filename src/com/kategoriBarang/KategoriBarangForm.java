    private void textKodeKategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textKodeKategoriActionPerformed

        try {
            String kode = textKodeKategori.getText();
            if (kode.equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(rootPane, "Barang Tidak Ditemukan");
            }
            String nama = KategoriKontrol.getKoneksiKategori().cariKategori(kode);
            textNamaKategori.setText(nama);
            tombolHapus.setEnabled(true);
        } catch (SQLException ex) {
            //Logger.getLogger(KategoriBarangForm.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("error = " + ex.getMessage());
        }


    }//GEN-LAST:event_textKodeKategoriActionPerformed

