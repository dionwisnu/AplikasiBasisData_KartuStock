    private void lihatKategori() {
        comboKategori.removeAllItems();
        try {
            List<KategoriBarang> daftarKategori = KategoriKontrol.getKoneksiKategori().lihatSeluruhKateogri();
            for (int i = 0; i < daftarKategori.size(); i++) {
                KategoriBarang kategoriBarang = daftarKategori.get(i);
                comboKategori.addItem(kategoriBarang.getKodeKategori() + " " + kategoriBarang.getNamaKategori());

            }
        } catch (SQLException ex) {
            Logger.getLogger(BarangForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
