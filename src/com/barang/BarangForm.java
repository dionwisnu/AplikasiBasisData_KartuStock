    private void buttonCetakGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCetakGroupActionPerformed
        // TODO add your handling code here:
        Connection kon = null;
        String reportSource = "";
        String kodeAwal = tfInKodeAwal.getText();
        String kodeAkhir = tfInKodeAkhir.getText();
        try {
            kon = new Koneksi().getDBConnection();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        if (cbPilihCetak.getSelectedItem().equals("Cetak Semua Data")) {
            reportSource = "./Laporan/Laporan.jasper";
        } else if (cbPilihCetak.getSelectedItem().equals("Cetak Beberapa Data")) {
            reportSource = "./Laporan/LaporanDenganParameter.jasper";
        }
        Map<String, Object> params = new HashMap<String, Object>();
        params.put(kodeAwal, kodeAwal);
        params.put(kodeAkhir, kodeAkhir);
        try {
            JasperPrint jasperPrint = JasperFillManager.fillReport(reportSource, params, kon);
            JasperViewer.viewReport(jasperPrint, false);
        } catch (JRException ex) {
            ex.printStackTrace();
        }


    }//GEN-LAST:event_buttonCetakGroupActionPerformed

