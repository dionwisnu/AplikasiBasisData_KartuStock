    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        try {
            int baris = tabelKartuStok.getSelectedRow();
            String kode = tabelKartuStok.getValueAt(baris, 1).toString();
            int reply = JOptionPane.showConfirmDialog(null, "Hapus Kartu Stok, Kode Transaksi " + kode, "Hapus Data", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                try {
                    KartuStokKontrol.getKoneksiKartuStok().HapusKartuStok(kode);
                    JOptionPane.showMessageDialog(null, "Hapus Berhasil");
                    tampilKartuStock();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Hapus Gagal : " + ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Data Tidak Dihapus");
            }
        } catch (java.lang.ArrayIndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(null, "Pilih Data yang Akan Dihapus");
        }
    }//GEN-LAST:event_buttonHapusActionPerformed

    public void tampilKartuStock() {
        try {
            List<KartuStok> kartustock = KartuStokKontrol.getKoneksiKartuStok().tampilKartuStock(tfKodeBarang.getText());
            KartuStokTableModel model = new KartuStokTableModel(kartustock);
            tabelKartuStok.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Gagal load : " + ex.getMessage());
        }
    }

