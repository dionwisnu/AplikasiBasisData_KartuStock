    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        try {

            Date tglPlh = pilihTanggal.getDate();
            DateFormat formatter = new SimpleDateFormat("dd-MMM-yy");
            String tglbaru = formatter.format(tglPlh);

            KartuStok ks = new KartuStok();
            Barang brg = new Barang();
            brg.setKodeBarang(tfKodeBarang.getText());
            ks.setKodeBarang(brg);
            ks.setTanggal(tglbaru);
            ks.setNoBukti(tfNoBukti.getText());
            ks.setKeterangan(tfKeterangan.getText());
            ks.setMasuk(Double.parseDouble(tfMasuk.getText()));
            ks.setKeluar(Double.parseDouble(tfKeluar.getText()));

            try {
                KartuStokKontrol kskontrol = KartuStokKontrol.getKoneksiKartuStok();
                kskontrol.tambahKartuStokPrepared(ks);
                System.out.println("Simpan berhasil");
                JOptionPane.showMessageDialog(this, "Data berhasil Disimpan");
                tampilKartuStock();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan");
                System.out.println("Error : " + ex.getMessage());
            }

            pilihTanggal.setDate(null);
            tfNoBukti.setText("");
            tfKeterangan.setText("");
            tfMasuk.setText("0");
            tfKeluar.setText("0");
            tfMasuk.setEnabled(true);
            tfMasuk.setEditable(true);
            tfKeluar.setEnabled(true);
            tfKeluar.setEditable(true);
        } catch (NullPointerException nulEx) {
            JOptionPane.showMessageDialog(null, "Data Belum Terisi", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed

    private void tfKodeBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfKodeBarangActionPerformed
        Barang brg = new Barang();
        String key = tfKodeBarang.getText();
        brg.setKodeBarang(key);
        try {
            try {
                KartuStokKontrol.getKoneksiKartuStok().cariBarang(brg);
                tfNamaBarang.setText(String.valueOf(brg.getNamaBarang()));
                tfKategori.setText(String.valueOf(brg.getKategori().getNamaKategori()));
                tfSatuan.setText(String.valueOf(brg.getSatuan()));
                tampilKartuStock();
                System.out.println("cari berhasil");
            } catch (java.lang.NullPointerException ex) {
                int reply = JOptionPane.showConfirmDialog(null, "Barang Tidak Ditemuakan, Cari Barang?", "Barang Tidak Ditemukan", JOptionPane.YES_NO_OPTION);
                if (reply == JOptionPane.YES_OPTION) {
                    tfKodeBarang.setText("");
                    tfNamaBarang.setText("");
                    internalFrameCariBarang.setVisible(true);
                    //JOptionPane.showMessageDialog(null, "HELLO");
                } else {
                    //JOptionPane.showMessageDialog(null, "GOODBYE");
                }
            }
        } catch (SQLException ex) {
            System.out.println("cari gagal : " + ex.getMessage());
        }
        buttonSimpan.setEnabled(true);
        buttonCetak.setEnabled(true);
        buttonBatal.setEnabled(true);
        buttonHapus.setEnabled(true);
        tfNamaBarang.setEditable(false);
        tfKategori.setEditable(false);
        tfSatuan.setEditable(false);
    }//GEN-LAST:event_tfKodeBarangActionPerformed

